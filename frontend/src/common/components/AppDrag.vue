<script setup>
import { DATA_TRANSFER_PAYLOAD } from "@/common/constants/constants";

const props = defineProps({
  draggable: {
    type: Boolean,
    default: false,
  },
  dataTransfer: {
    type: Object,
    required: true,
  },
});

const onDragStart = ({ dataTransfer }) => {
  const data = JSON.stringify(props.dataTransfer);
  dataTransfer.setData(DATA_TRANSFER_PAYLOAD, data);
};
</script>

<template>
  <div
      :draggable="draggable"
      @dragstart.self="onDragStart"
      @dragover.prevent
      @dragenter.prevent
  >
    <slot />
  </div>
</template>

